<h2>Bilgisayarın Tarihçesi Nedir?</h2>
<p>Hesaplamaya yardımcı cihazlar tarih boyunca insanların yardımcısı olmuştur. Ticaretin gelişmesiye ve satışların büyük miktarlara ulaşmasıyla insanlar karmaşık sayma işlemlerine ihtiyaç duydular ve Babil İmparatorluğu döneminde (M.Ö. 2400’lü) bilgisayarın atası olarak kabul edilen abaküsü kullanmaya başladılar. Takip eden yıllar boyunca insanoğlu bugünkü anlamda bilgisayarın gelişmesine yardımcı olan birçok mekanik ve elektro-mekanik aletler geliştirdi.</p>

<h2>İlk Bilgisayar</h2>
<p>Bugün anladığımız anlamda ilk dijital bilgisayar icat tarihi ise 1946’dır. ENIAC adlı ilk bilgisayar ABD Ordusu’nun top atışları hesaplamalarına yardımcı olması için Amerikan Ordusu’nun desteği ile geliştirilmiştir. Kendinden önceki elektro-mekanik cihazların bin katı işlem gücüne sahip olan ENIAC’ın geliştirmesi zamanın parasıyla 500 bin dolar tutmuştur. Bu miktarın bugünkü değeri 6 milyon dolardır. ENIAC, 1955 yılının ekim ayına kadar kullanılmıştır.</p>

